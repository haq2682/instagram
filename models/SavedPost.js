const mongoose = require('mongoose');
const {Schema} = mongoose;

const savedPostSchema = new Schema({
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    post: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Post',
        required: true
    },
    created_at: {
        type: Date,
        default: new Date()
    },
    updated_at: {
        type: Date,
        default: new Date()
    }
});

const SavedPost = mongoose.model('SavedPost', savedPostSchema);

module.exports = SavedPost;